# Tipos de variables y forma de declararlas

texto: str = "Valor en cadena"
numero: int = 1
lista: list = [1, 2, 3, 4]
tupla: tuple = (1, 2, 3, 4)
diccionario: dict = {"key": "1234", "value": "Esto es un objecto"}

print(
    {
        "texto": texto,
        "numero": numero,
        "lista": lista,
        "tupla": tupla,
        "diccionario": diccionario,
    }
)
